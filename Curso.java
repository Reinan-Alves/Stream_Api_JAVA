package streamApi;

public class Curso {
	
	final String nome;
	final double avaliacao;
	final double duracao;
	final boolean recomendado;
	
	public Curso(String nome, double avaliacao, double duracao, boolean recomendado) {
		this.nome = nome;
		this.avaliacao = avaliacao;
		this.duracao = duracao;
		this.recomendado = recomendado;
	}
	
	
	

}
